def arah_labeling(bmp, x, y, arah):
    # arah: 2=kanan, 3=bawah, 4=kiri, 5=atas
    if bmp[x][y] != 1:
        return
    bmp[x][y] = arah
    
    # kanan
    if y+1 < len(bmp[0]) and bmp[x][y+1] == 1:
        arah_labeling(bmp, x, y+1, 2)
    # bawah
    if x+1 < len(bmp) and bmp[x+1][y] == 1:
        arah_labeling(bmp, x+1, y, 3)
    # kiri
    if y-1 >= 0 and bmp[x][y-1] == 1:
        arah_labeling(bmp, x, y-1, 4)
    # atas
    if x-1 >= 0 and bmp[x-1][y] == 1:
        arah_labeling(bmp, x-1, y, 5)


